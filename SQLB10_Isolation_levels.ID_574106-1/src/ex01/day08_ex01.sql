-- КОМАНДЫ ДЛЯ СЕССИИ #1
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции.
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

-- 3. Проверяем текущий уровень изоляции.
SHOW TRANSACTION ISOLATION LEVEL;

-- 4. Считываем текущий рейтинг 'Pizza Hut'.
-- Ожидаемый результат: 5.
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';

-- 5. Обновляем рейтинг на 4. Это изменение пока не зафиксировано.
UPDATE pizzeria SET rating = 4 WHERE name = 'Pizza Hut';

-- 6. !!! НЕ ДЕЛАЕМ COMMIT СРАЗУ !!!
-- Ждем, пока Сессия #2 прочитает данные и сделает свой UPDATE.
-- После того как Сессия #2 выполнит свой UPDATE, возвращаемся сюда.

-- 7. Фиксируем изменения Сессии #1.
COMMIT;

-- 8. Проверяем итоговый рейтинг после всех операций.
-- Ожидаемый результат: 3.6 (если Сессия #2 зафиксировалась позже).
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';


--------------------------------------------------------------------------------
-- КОМАНДЫ ДЛЯ СЕССИИ #2
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции.
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

-- 3. Проверяем текущий уровень изоляции.
