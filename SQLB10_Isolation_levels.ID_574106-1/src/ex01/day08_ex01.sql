-- 4. Считываем текущий рейтинг 'Pizza Hut'.
-- Ожидаемый результат: 5,
-- так как изменения Сессии #1 еще не зафиксированы.
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';

-- 5. Обновляем рейтинг на 3.6. Это изменение пока не зафиксировано.
UPDATE pizzeria SET rating = 3.6 WHERE name = 'Pizza Hut';

-- 6. !!! НЕ ДЕЛАЕМ COMMIT СРАЗУ !!!
-- Ждем, пока Сессия #1 сделает свой COMMIT.
-- После того как Сессия #1 выполнит COMMIT, возвращаемся сюда.

-- 7. Фиксируем изменения Сессии #2.
COMMIT;

-- 8. Проверяем итоговый рейтинг после всех операций.
-- Ожидаемый результат: 3.6 (поскольку это последнее зафиксированное значение).
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';