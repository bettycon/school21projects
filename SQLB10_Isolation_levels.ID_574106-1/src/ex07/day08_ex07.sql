-- КОМАНДЫ ДЛЯ СЕССИИ #1
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Обновляем рейтинг 'Pizza Hut'. Это ставит блокировку на эту строку.
UPDATE pizzeria SET rating = 4.0 WHERE name = 'Pizza Hut';

-- 3. !!! НЕ ДЕЛАЕМ НИЧЕГО !!!
-- Ждем, пока Сессия #2 обновит другую пиццерию ('Dominos').
-- После того как Сессия #2 выполнит свой ПЕРВЫЙ UPDATE, возвращаемся сюда.

-- 4. Пытаемся обновить рейтинг 'Dominos'.
-- Эта строка уже заблокирована Сессией #2.
-- Сессия #1 встает в очередь и ждет, пока Сессия #2 освободит 'Dominos'.
UPDATE pizzeria SET rating = 4.1 WHERE name = 'Dominos';

-- 5. Завершаем транзакцию.
-- ВАЖНО: одна из транзакций (эта или вторая) будет прервана СУБД с ошибкой deadlock.
COMMIT;


--------------------------------------------------------------------------------
-- КОМАНДЫ ДЛЯ СЕССИИ #2
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Обновляем рейтинг 'Dominos'. Это ставит блокировку на эту строку.
UPDATE pizzeria SET rating = 4.2 WHERE name = 'Dominos';

-- 3. !!! НЕ ДЕЛАЕМ НИЧЕГО !!!
-- Ждем, пока Сессия #1 попытается обновить 'Dominos'.

-- 4. Пытаемся обновить рейтинг 'Pizza Hut'.
-- Эта строка уже заблокирована Сессией #1.
-- Когда Сессия #2 пытается заблокировать этот ресурс, PostgreSQL обнаруживает
-- перекрестное ожидание (deadlock) и принудительно завершает одну из транзакций.
-- Скорее всего, именно эта команда и вызовет ошибку.
UPDATE pizzeria SET rating = 4.3 WHERE name = 'Pizza Hut';

-- 5. Завершаем транзакцию.
-- ВАЖНО: одна из транзакций (эта или первая) будет прервана СУБД с ошибкой deadlock.
COMMIT;