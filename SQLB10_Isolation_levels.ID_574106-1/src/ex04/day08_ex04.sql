-- КОМАНДЫ ДЛЯ СЕССИИ #1
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции SERIALIZABLE.
SET TRANSACTION ISOLATION LEVEL SERIALIZABLE;

-- 3. Проверяем текущий уровень изоляции.
SHOW TRANSACTION ISOLATION LEVEL;

-- 4. ПЕРВОЕ чтение рейтинга 'Pizza Hut'.
-- Ожидаемый результат: 5.
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';

-- 5. !!! НЕ ДЕЛАЕМ НИЧЕГО !!!
-- Ждем, пока Сессия #2 обновит и зафиксирует свои изменения.
-- После того как Сессия #2 выполнит UPDATE и COMMIT, возвращаемся сюда.

-- 6. ВТОРОЕ чтение рейтинга 'Pizza Hut' в той же транзакции.
-- Ожидаемый результат: 5.
-- Это демонстрирует, что SERIALIZABLE предотвращает неповторяющееся чтение.
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';

-- 7. Фиксируем транзакцию Сессии #1.
COMMIT;


--------------------------------------------------------------------------------
-- КОМАНДЫ ДЛЯ СЕССИИ #2
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции SERIALIZABLE.
SET TRANSACTION ISOLATION LEVEL SERIALIZABLE;

-- 3. Проверяем текущий уровень изоляции.
SHOW TRANSACTION ISOLATION LEVEL;

-- 4. !!! НЕ ДЕЛАЕМ НИЧЕГО !!!
-- Ждем, пока Сессия #1 выполнит свое ПЕРВОЕ чтение.

-- 5. Обновляем рейтинг 'Pizza Hut' на 3.0.
UPDATE pizzeria SET rating = 3.0 WHERE name = 'Pizza Hut';

-- 6. Фиксируем изменения Сессии #2.
COMMIT;