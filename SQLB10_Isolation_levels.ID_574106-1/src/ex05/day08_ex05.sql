-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции.
SET TRANSACTION ISOLATION LEVEL READ COMMITTED;

-- 3. Проверяем текущий уровень изоляции.
SHOW TRANSACTION ISOLATION LEVEL;

-- 4. !!! НЕ ДЕЛАЕМ НИЧЕГО !!!
-- Ждем, пока Сессия #1 выполнит свой ПЕРВЫЙ запрос агрегации.

-- 5. Вставляем новую пиццерию 'Kazan Pizza' с ID=10 и рейтингом 5.
INSERT INTO pizzeria (id, name, rating) VALUES (10, 'Kazan Pizza', 5);

-- 6. Фиксируем изменения Сессии #2.
COMMIT;