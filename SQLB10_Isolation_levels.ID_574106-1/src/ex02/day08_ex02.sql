-- КОМАНДЫ ДЛЯ СЕССИИ #1
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции REPEATABLE READ.
SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;

-- 3. Проверяем текущий уровень изоляции.
SHOW TRANSACTION ISOLATION LEVEL;

-- 4. Считываем текущий рейтинг 'Pizza Hut'.
-- Ожидаемый результат: 5.
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';

-- 5. Обновляем рейтинг на 4. Это изменение пока не зафиксировано.
UPDATE pizzeria SET rating = 4 WHERE name = 'Pizza Hut';

-- 6. !!! НЕ ДЕЛАЕМ COMMIT СРАЗУ !!!
-- Ждем, пока Сессия #2 прочитает данные и сделает свой UPDATE.
-- После того как Сессия #2 выполнит свой UPDATE, возвращаемся сюда.

-- 7. Фиксируем изменения Сессии #1.
COMMIT;

-- 8. Проверяем итоговый рейтинг после всех операций.
-- Ожидаемый результат: если эта транзакция зафиксировалась первой, то 4.
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';


--------------------------------------------------------------------------------
-- КОМАНДЫ ДЛЯ СЕССИИ #2
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции REPEATABLE READ.
SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;

-- 3. Проверяем текущий уровень изоляции.
SHOW TRANSACTION ISOLATION LEVEL;

-- 4. Считываем текущий рейтинг 'Pizza Hut'.
-- Ожидаемый результат: 5,
-- так как изменения Сессии #1 еще не зафиксированы.
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';

-- 5. Обновляем рейтинг на 3.6. Это изменение пока не зафиксировано.
UPDATE pizzeria SET rating = 3.6 WHERE name = 'Pizza Hut';

-- 6. !!! НЕ ДЕЛАЕМ COMMIT СРАЗУ !!!
-- Ждем, пока Сессия #1 сделает свой COMMIT.
-- После того как Сессия #1 выполнит COMMIT, возвращаемся сюда.

-- 7. Фиксируем изменения Сессии #2.
COMMIT;

-- 8. Проверяем итоговый рейтинг после всех операций.
-- Ожидаемый результат: Эта транзакция, скорее всего, столкнется с ошибкой
-- "ERROR: could not serialize access due to concurrent update" при попытке COMMIT,
-- так как REPEATABLE READ предотвращает аномалии Lost Update.
SELECT * FROM pizzeria WHERE name = 'Pizza Hut';