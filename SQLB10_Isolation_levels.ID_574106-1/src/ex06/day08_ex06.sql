-- КОМАНДЫ ДЛЯ СЕССИИ #1
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции REPEATABLE READ.
SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;

-- 3. Проверяем текущий уровень изоляции.
SHOW TRANSACTION ISOLATION LEVEL;

-- 4. ПЕРВЫЙ запрос агрегации (SUM) по рейтингам всех пиццерий.
-- Запомним этот результат.
SELECT SUM(rating) FROM pizzeria;

-- 5. !!! НЕ ДЕЛАЕМ НИЧЕГО !!!
-- Ждем, пока Сессия #2 вставит новую пиццерию и зафиксирует изменения.
-- После того как Сессия #2 выполнит INSERT и COMMIT, возвращаемся сюда.

-- 6. ВТОРОЙ запрос агрегации (SUM) по рейтингам всех пиццерий в той же транзакции.
-- Ожидаемый результат: Сумма будет ТАКОЙ ЖЕ, как и при первом чтении.
-- REPEATABLE READ в PostgreSQL работает со "снимком" данных, сделанным в начале транзакции,
-- поэтому он не видит новые строки, зафиксированные другими транзакциями.
-- Таким образом, аномалия "Фантомного чтения" предотвращается.
SELECT SUM(rating) FROM pizzeria;

-- 7. Фиксируем транзакцию Сессии #1.
COMMIT;


--------------------------------------------------------------------------------
-- КОМАНДЫ ДЛЯ СЕССИИ #2
--------------------------------------------------------------------------------

-- 1. Начинаем транзакцию.
BEGIN;

-- 2. Устанавливаем уровень изоляции REPEATABLE READ.
SET TRANSACTION ISOLATION LEVEL REPEATABLE READ;

-- 3. Проверяем текущий уровень изоляции.
SHOW TRANSACTION ISOLATION LEVEL;

-- 4. !!! НЕ ДЕЛАЕМ НИЧЕГО !!!
-- Ждем, пока Сессия #1 выполнит свой ПЕРВЫЙ запрос агрегации.

-- 5. Вставляем новую пиццерию 'Kazan Pizza 2' с ID=11 и рейтингом 4.
-- Убедитесь, что ID=11 не занят.
INSERT INTO pizzeria (id, name, rating) VALUES (11, 'Kazan Pizza 2', 4);

-- 6. Фиксируем изменения Сессии #2.
COMMIT;