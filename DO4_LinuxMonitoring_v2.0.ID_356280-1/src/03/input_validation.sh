validate_input() {
    if [ $# -lt 1 ]; then
        echo "Ошибка: Укажите метод очистки (1, 2 или 3)"
        echo "1 - По файлу журнала"
        echo "2 - По дате создания"
        echo "3 - По маске имени"
        exit 1
    fi

    if ! [[ $1 =~ ^[1-3]$ ]]; then
        echo "Ошибка: Неверный метод. Допустимо: 1, 2 или 3"
        exit 1
    fi

    # Дополнительная проверка для метода 2
    if [ $1 -eq 2 ] && [ $# -lt 3 ]; then
        echo "Для метода 2 можно указать даты как параметры:"
        echo "$0 2 \"YYYY-MM-DD HH:MM\" \"YYYY-MM-DD HH:MM\""
        echo "Или ввести их во время выполнения"
    fi
}
