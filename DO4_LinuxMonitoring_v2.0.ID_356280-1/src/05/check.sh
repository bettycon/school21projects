#!/bin/bash

if [[ $# -ne 1 ]]; then
    echo "Ошибка: требуется ровно один параметр"
    echo "Использование: $0 <1-4>"
    exit 1
fi

if ! [[ $1 =~ ^[1-4]$ ]]; then
    echo "Ошибка: параметр должен быть числом от 1 до 4"
    echo "Доступные варианты:"
    echo "1 - Сортировка по коду ответа"
    echo "2 - Показать уникальные IP"
    echo "3 - Показать ошибочные запросы"
    echo "4 - Показать IP с ошибочными запросами"
    exit 1
fi
