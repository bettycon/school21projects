#!/bin/bash

echo "🧪 Запуск интеграционных тестов..."
echo "=========================================="

# Переходим в папку с бинарником
cd ../code-samples

# Тест 1: Проверка без аргументов (должен вернуть 255)
echo "Тест 1: Вызов без аргументов"
./DO
exit_code=$?
if [ $exit_code -eq 255 ]; then
    echo "✅ Тест 1 passed: Правильная обработка отсутствия аргументов (код: $exit_code)"
else
    echo "❌ Тест 1 failed: Ожидался код 255, получен $exit_code"
    exit 1
fi

echo "------------------------------------------"

# Тест 2: Проверка с неправильным числом аргументов
echo "Тест 2: Слишком много аргументов"
./DO 1 2 3
exit_code=$?
if [ $exit_code -eq 255 ]; then
    echo "✅ Тест 2 passed: Правильная обработка избытка аргументов (код: $exit_code)"
else
    echo "❌ Тест 2 failed: Ожидался код 255, получен $exit_code"
    exit 1
fi

echo "------------------------------------------"

# Тест 3: Проверка валидных значений (1-6)
echo "Тест 3: Проверка валидных значений 1-6"
for i in {1..6}; do
    echo "Тест 3.$i: Проверка значения $i"
    output=$(./DO $i 2>&1)
    exit_code=$?
    if [ $exit_code -eq 0 ]; then
        echo "✅ Тест 3.$i passed: Значение $i обработано успешно (код: $exit_code)"
        echo "   Вывод: $output"
    else
        echo "❌ Тест 3.$i failed: Значение $i не обработано (код: $exit_code)"
        echo "   Вывод: $output"
        exit 1
    fi  # БЫЛА ОШИБКА - НЕ БЫЛО ЗАКРЫТИЯ IF
done

echo "------------------------------------------"

# Тест 4: Проверка невалидного значения
echo "Тест 4: Невалидное значение (7)"
output=$(./DO 7 2>&1)
exit_code=$?
if [ $exit_code -eq 254 ]; then
    echo "✅ Тест 4 passed: Правильная обработка невалидного значения (код: $exit_code)"
    echo "   Вывод: $output"
else
    echo "❌ Тест 4 failed: Ожидался код 254, получен $exit_code"
    echo "   Вывод: $output"
    exit 1
fi

echo "------------------------------------------"

# Тест 5: Проверка отрицательного значения
echo "Тест 5: Отрицательное значение (-1)"
output=$(./DO -1 2>&1)
exit_code=$?
if [ $exit_code -eq 254 ]; then
    echo "✅ Тест 5 passed: Правильная обработка отрицательного значения (код: $exit_code)"
    echo "   Вывод: $output"
else
    echo "❌ Тест 5 failed: Ожидался код 254, получен $exit_code"
    echo "   Вывод: $output"
    exit 1
fi

echo "=========================================="
echo "🎉 Все интеграционные тесты успешно пройдены!"
echo "✅ Приложение работает корректно!"